package com.spx.entity;

public class Computer {

    int id;
    int cpuid;
    int dyid;
    int ncid;
    int xkid;
    int xsqid;
    int ypid;
    int zbid;
    int userid;
    int fs;
    String ms;
    String more;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCpuid() {
        return cpuid;
    }

    public void setCpuid(int cpuid) {
        this.cpuid = cpuid;
    }

    public int getDyid() {
        return dyid;
    }

    public void setDyid(int dyid) {
        this.dyid = dyid;
    }

    public int getNcid() {
        return ncid;
    }

    public void setNcid(int ncid) {
        this.ncid = ncid;
    }

    public int getXkid() {
        return xkid;
    }

    public void setXkid(int xkid) {
        this.xkid = xkid;
    }

    public int getXsqid() {
        return xsqid;
    }

    public void setXsqid(int xsqid) {
        this.xsqid = xsqid;
    }

    public int getYpid() {
        return ypid;
    }

    public void setYpid(int ypid) {
        this.ypid = ypid;
    }

    public int getZbid() {
        return zbid;
    }

    public void setZbid(int zbid) {
        this.zbid = zbid;
    }

    public int getUserid() {
        return userid;
    }

    public void setUserid(int userid) {
        this.userid = userid;
    }

    public int getFs() {
        return fs;
    }

    public void setFs(int fs) {
        this.fs = fs;
    }

    public String getMs() {
        return ms;
    }

    public void setMs(String ms) {
        this.ms = ms;
    }

    public String getMore() {
        return more;
    }

    public void setMore(String more) {
        this.more = more;
    }
}
